namespace FaroreUtil {
  public class LengthInfo {
    //::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::
    // Field
    //::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::

    public bool IsInfinite { get; private set;}
    public int  Length     { get; private set;}

    //::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::
    // Constructor
    //::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::

    public LengthInfo (int length) {
      IsInfinite = (length == 0);
      Length     = length;
    }
  }
}
